// Program som konverterar fahrenheit till celsius
Console.WriteLine("Program som konverterar fahrenheit till celsius");

// Ange värde i fahrenheit
Console.WriteLine("Ange värdet i fahrenheit");
double = tempFahrenheit = double.Parse(Console.ReadLine());


// Konvertera fahrenheit till celsius
double tempCelsius = (tempFahrenheit -32) * 5 / 9

// Ange det konverterade värdet i celsius med decimaler
Console.WriteLine($"{tempFahrenheit} blir {tempCelsius} Celsius");